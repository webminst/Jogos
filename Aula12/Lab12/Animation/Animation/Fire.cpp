#include "Fire.h"
#include "Anima.h"

// ---------------------------------------------------------------------------------

Fire::Fire(TileSet* tiles)
{
    anim = new Animation(tiles, 0.04f, true);
}

// ---------------------------------------------------------------------------------

Fire::~Fire()
{
    delete anim;
}

// ---------------------------------------------------------------------------------

void Fire::Update()
{
    anim->NextFrame();
    if (anim->Inactive())
        Anima::scene->Delete();
}

